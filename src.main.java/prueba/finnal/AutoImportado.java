package prueba.finnal;

public class AutoImportado {
	
	private String codigoImportación;
	private String valorImportación;
	
	public String getCodigoImportación() {
		return codigoImportación;
	}
	public void setCodigoImportación(String codigoImportación) {
		this.codigoImportación = codigoImportación;
	}
	public String getValorImportación() {
		return valorImportación;
	}
	public void setValorImportación(String valorImportación) {
		this.valorImportación = valorImportación;
	}
	
}
